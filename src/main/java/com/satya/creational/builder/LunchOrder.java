package com.satya.creational.builder;

public class LunchOrder {
    private String bread;
    private String confiments;
    private String dreassing;
    private String meat;

    public String getBread() {
        return bread;
    }

    public void setBread(String bread) {
        this.bread = bread;
    }

    public String getConfiments() {
        return confiments;
    }

    public void setConfiments(String confiments) {
        this.confiments = confiments;
    }

    public String getDreassing() {
        return dreassing;
    }

    public void setDreassing(String dreassing) {
        this.dreassing = dreassing;
    }

    public String getMeat() {
        return meat;
    }

    public void setMeat(String meat) {
        this.meat = meat;
    }
}
